package org.kuali.mobility.calendars.service;

import javax.xml.bind.annotation.XmlRootElement;

/**
 * Event class describes an event
 *
 * @author Nurul Haque Murshed <nurul.murshed@htcindia.com>
 */
@XmlRootElement(name = "eventDetails")
public class Event {

    private int eventId;
    private String eventDate;
    private String startTime;
    private String endTime;
    private String title;
    private String eventBody;

    /**
     * @return the eventId
     */
    public int getEventId() {
        return eventId;
    }

    /**
     * @param eventId the eventId to set
     */
    public void setEventId(int eventId) {
        this.eventId = eventId;
    }

    /**
     * @return the eventDate
     */
    public String getEventDate() {
        return eventDate;
    }

    /**
     * @param eventDate the eventDate to set
     */
    public void setEventDate(String eventDate) {
        this.eventDate = eventDate;
    }

    /**
     * @return the startTime
     */
    public String getStartTime() {
        return startTime;
    }

    /**
     * @param startTime the startTime to set
     */
    public void setStartTime(String startTime) {
        this.startTime = startTime;
    }

    /**
     * @return the endTime
     */
    public String getEndTime() {
        return endTime;
    }

    /**
     * @param endTime the endTime to set
     */
    public void setEndTime(String endTime) {
        this.endTime = endTime;
    }

    /**
     * @return the title
     */
    public String getTitle() {
        return title;
    }

    /**
     * @param title the title to set
     */
    public void setTitle(String title) {
        this.title = title;
    }

    /**
     * @return the eventBody
     */
    public String getEventBody() {
        return eventBody;
    }

    /**
     * @param eventBody the eventBody to set
     */
    public void setEventBody(String eventBody) {
        this.eventBody = eventBody;
    }
}
