package org.kuali.mobility.feedback.service;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.junit.*;
import org.junit.runner.RunWith;
import org.kuali.mobility.feedback.entity.Feedback;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.beans.factory.annotation.Qualifier;
import org.springframework.test.context.ContextConfiguration;
import org.springframework.test.context.junit4.SpringJUnit4ClassRunner;

import javax.persistence.EntityManager;
import javax.persistence.PersistenceContext;

import static org.junit.Assert.assertTrue;

/**
 * @author Kuali Mobility Team (mobility.collab@kuali.org)
 */
@RunWith(SpringJUnit4ClassRunner.class)
@ContextConfiguration(value = "classpath:TestSpringBeans.xml")
public class FeedbackServiceImplTest {

	private static final Logger LOG = LoggerFactory.getLogger(FeedbackServiceImplTest.class);

	@PersistenceContext
	private EntityManager entityManager;

	@Autowired
	@Qualifier("feedbackService")
	private FeedbackService feedbackService;

	@BeforeClass
	public static void setUpBeforeClass() throws Exception {
	}

	@AfterClass
	public static void tearDownAfterClass() throws Exception {
	}

	@Before
	public void setUp() throws Exception {
	}

	@After
	public void tearDown() throws Exception {
	}

	@Test
	public void testSaveFeedback() {
		Feedback f1 = new Feedback();
		f1.setEmail("Test mail");
		feedbackService.saveFeedback(f1);
		LOG.debug("Feedback id :" + f1.getFeedbackId());
		assertTrue("Feedback save failed", f1.getFeedbackId() != null);
	}

	public FeedbackService getFeedbackService() {
		return feedbackService;
	}

	public void setFeedbackService(FeedbackService feedbackService) {
		this.feedbackService = feedbackService;
	}

}
