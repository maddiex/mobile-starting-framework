package org.kuali.mobility.shared.filters;

import java.io.IOException;

import javax.servlet.Filter;
import javax.servlet.FilterChain;
import javax.servlet.FilterConfig;
import javax.servlet.ServletException;
import javax.servlet.ServletRequest;
import javax.servlet.ServletResponse;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletRequestWrapper;
import javax.servlet.http.HttpServletResponse;

import org.kuali.mobility.shared.Constants;
import org.kuali.mobility.shared.entity.MockUser;

public class MockSSOFilter implements Filter {

	@Override
	public void init(FilterConfig filterConfig) throws ServletException {
	}

	@Override
	public void destroy() {
	}

	@Override
	public void doFilter(ServletRequest srequest, ServletResponse sresponse, FilterChain chain) throws IOException, ServletException {
		HttpServletRequest request = (HttpServletRequest) srequest;
		HttpServletResponse response = (HttpServletResponse) sresponse;

		MockUser mockUser = (MockUser) request.getSession().getAttribute(Constants.KME_MOCK_USER_KEY);
		if (mockUser != null) {
			final String remoteUser = mockUser.getUserId();
			if (remoteUser != null) {
				request = new HttpServletRequestWrapper(request) {
					public String getRemoteUser() {
						return remoteUser;
					}
				};
			}
		}
		chain.doFilter(request, response);
	}

}
